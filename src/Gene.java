public class Gene {
	public String id;
	public String assayKey;
	public String resultKey;
	public String markerKey;
	public String numericPart;
	public String symbol;
	public String name;
	public String assayTypeKey;
	public String structureName;
	public String structureKey;
	public String edinburghKey;
	public String strength;
	
	public Gene(){
		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getAssayKey() {
		return assayKey;
	}

	public void setAssayKey(String assayKey) {
		this.assayKey = assayKey;
	}

	public String getResultKey() {
		return resultKey;
	}

	public void setResultKey(String resultKey) {
		this.resultKey = resultKey;
	}

	public String getMarkerKey() {
		return markerKey;
	}

	public void setMarkerKey(String markerKey) {
		this.markerKey = markerKey;
	}

	public String getNumericPart() {
		return numericPart;
	}

	public void setNumericPart(String numericPart) {
		this.numericPart = numericPart;
	}

	public String getSymbol() {
		return symbol;
	}

	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAssayTypeKey() {
		return assayTypeKey;
	}

	public void setAssayTypeKey(String assayTypeKey) {
		this.assayTypeKey = assayTypeKey;
	}

	public String getStructureName() {
		return structureName;
	}

	public void setStructureName(String structureName) {
		this.structureName = structureName;
	}

	public String getStructureKey() {
		return structureKey;
	}

	public void setStructureKey(String structureKey) {
		this.structureKey = structureKey;
	}

	public String getEdinburghKey() {
		return edinburghKey;
	}

	public void setEdinburghKey(String edinburghKey) {
		this.edinburghKey = edinburghKey;
	}

	public String getStrength() {
		return strength;
	}

	public void setStrength(String strength) {
		this.strength = strength;
	}
}
